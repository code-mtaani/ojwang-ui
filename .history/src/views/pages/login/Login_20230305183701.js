import React, { useState } from 'react'
import { Link } from 'react-router-dom'
import {
  CButton,
  CCard,
  CCardBody,
  CCardGroup,
  CCol,
  CContainer,
  CForm,
  CFormInput,
  CInputGroup,
  CInputGroupText,
  CRow,
} from '@coreui/react'
import CIcon from '@coreui/icons-react'
import { cilLockLocked, cilUser } from '@coreui/icons'
import { axiosPublic } from 'src/utils/axiosPublic'
// import { useDispatch, useSelector } from 'react-redux'

const Login = () => {
  const [loginLoadingShow, setLoginLoadingShow] = useState(false)
  const [loginErrorMessageShow, setLoginErrorMessageShow] = useState(false)
  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')

  const toggleAlert = () => {
    setLoginErrorMessageShow(!loginErrorMessageShow)
  }

  const handleSubmit = async (e) => {
    e.preventDefault()
    setLoginLoadingShow(!loginLoadingShow)
    const payload = JSON.stringify({ email, password })

    const onSuccess = ({ data }) => {
      setLoginLoadingShow(!loginLoadingShow)
      console.log('Logged in successfully')
      localStorage.setItem('session', JSON.stringify(data))
      window.location.reload()
    }

    const onFailure = (error) => {
      toggleAlert()
      console.log('Login failed.' + error && error.response)
    }
    axiosPublic.post( '/v1/user/login/', payload ).then( onSuccess ).catch( onFailure )
    setEmail("")
    setPassword("")
  }

  return (
    <div className="bg-light min-vh-100 d-flex flex-row align-items-center">
      <CContainer>
        <CRow className="justify-content-center">
          <CCol md={8}>
            <CCardGroup>
              <CCard className="p-4">
                <CCardBody>
                  <CForm onSubmit={handleSubmit}>
                    <h1>Login</h1>
                    <p className="text-medium-emphasis">Sign In to your ojwang account</p>
                    <CInputGroup className="mb-3">
                      <CInputGroupText>
                        <CIcon icon={cilUser} />
                      </CInputGroupText>
                      <CFormInput
                        placeholder="Email"
                        autoComplete="email"
                        type="email"
                        id="email"
                        onChange={(e) => setEmail(e.target.value)}
                      />
                    </CInputGroup>
                    <CInputGroup className="mb-4">
                      <CInputGroupText>
                        <CIcon icon={cilLockLocked} />
                      </CInputGroupText>
                      <CFormInput
                        type="password"
                        id="password"
                        placeholder="Password"
                        autoComplete="current-password"
                        onChange={(e) => setPassword(e.target.value)}
                      />
                    </CInputGroup>
                    <CRow>
                      <CCol xs={6}>
                        <CButton type="submit" color="primary" className="px-4">
                          Login
                        </CButton>
                      </CCol>
                      <CCol xs={6} className="text-right">
                        <CButton color="link" className="px-0">
                          Forgot password?
                        </CButton>
                      </CCol>
                    </CRow>
                  </CForm>
                </CCardBody>
              </CCard>
              <CCard className="text-white bg-primary py-5" style={{ width: '44%' }}>
                <CCardBody className="text-center">
                  <div>
                    <h2>Sign up</h2>
                    <p>To get started,register your account.</p>
                    <Link to="/register">
                      <CButton color="primary" className="mt-3" active tabIndex={-1}>
                        Register Now!
                      </CButton>
                    </Link>
                  </div>
                </CCardBody>
              </CCard>
            </CCardGroup>
          </CCol>
        </CRow>
      </CContainer>
    </div>
  )
}

export default Login
